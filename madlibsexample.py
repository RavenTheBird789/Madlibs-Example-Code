# madlibs example code

import random

a = input("Would you like to play madlibs? (y/n): ")
if a == "y":
    def madlibs():
        verb1 = input("Choose a verb: ")
        verb2 = input("Choose a verb: ")
        adj = input("Choose an adjective: ")
        noun = input("Choose a noun: ")

        sentence1 = f"I can't wait to {verb1} after school with my {adj} {noun} then {verb2} with my mom when I get home!";
        sentence2 = f"Once it's summer I plan to {verb1} at the beach with my {noun} and {verb2} ice cream in my {adj} car.";
        
        b = [sentence1, sentence2]
        print(random.choice(b));
        
        c = input("Would you like to play again? (y/n): ");
        if c == "y":
            madlibs();
        elif c == "Y":
            madlibs();
        elif c == "yes":
            madlibs();
        elif c == "Yes":
            madlibs();
        elif c == "n":
            print("Have a nice day!");
        elif c == "no":
            print("Have a nice day!");
        elif c == "N":
            print("Have a nice day!");
        elif c == "No":
            print("Have a nice day!");
        else:
            print("Invalid Input")
    madlibs();
